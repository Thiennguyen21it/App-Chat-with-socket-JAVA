    private void btnConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectActionPerformed
        // TODO add your handling code here:
        mngIP = this.txtServerIP.getText(); // get server IP and server Port
        mngPort = Integer.parseInt(this.txtServerPort.getText());
        staffName = this.txtStaff.getText();
        try{
           mngSocket = new Socket(mngIP,mngPort); // connect to server by server ip and server port
           if(mngSocket != null) {
               //create chat component and add it to the GUI
               ChatPanel p = new ChatPanel(mngSocket, staffName, "Manager");
               p.updateUI();
               bf = new BufferedReader(new InputStreamReader(mngSocket.getInputStream()));
               os = new DataOutputStream(mngSocket.getOutputStream());
               os.writeBytes("Staff : " + staffName);
               os.writeByte(13);
               os.writeByte(10);
               os.flush();
           }
        }catch(Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnConnectActionPerformed

